#!/bin/bash

set -e

########################################
# 防止递归调用
########################################
if [ "$CLAUDE_SUMMARY_INTERNAL" = "true" ] || [ "$CLAUDE_SUMMARY_INTERNAL" = "1" ]; then
  exit 0
fi

########################################
# 检查是否存在代码变更
########################################
if git rev-parse --git-dir >/dev/null 2>&1; then
  if git diff --quiet HEAD 2>/dev/null; then
    echo "✅ 无代码变更，跳过工作总结"
    exit 0
  fi
fi

echo "🔄 正在生成本次工作总结..."

########################################
# 环境变量
########################################
export CLAUDE_SUMMARY_INTERNAL="true"

########################################
# 工作日志目录
########################################
WORKLOG_DIR="worklogs"

TODAY=$(date "+%Y-%m-%d")
NOW=$(date "+%Y-%m-%d %H:%M:%S")
CURRENT_TIME=$(date "+%H:%M")

mkdir -p "$WORKLOG_DIR"

WORKLOG_FILE="${WORKLOG_DIR}/${TODAY}.md"

########################################
# 当天文件不存在则初始化
########################################
if [ ! -f "$WORKLOG_FILE" ]; then
cat > "$WORKLOG_FILE" << EOF
# 工作日志 - ${TODAY}

> 自动生成于 Claude Code Stop Hook

---

EOF
fi

########################################
# Prompt
########################################
PROMPT_CONTENT=$(cat << EOF
你现在是一名资深软件架构师、技术经理和敏捷开发专家。

请分析：

1. 当前工作区最新代码改动
2. Git Diff
3. Git Status
4. 当前任务上下文
5. 最近完成的工作

然后将下面内容追加写入文件：

${WORKLOG_FILE}

要求：

- 必须追加，不允许覆盖已有内容
- 内容必须是 Markdown
- 不要输出解释
- 不要输出代码块
- 直接输出需要追加的内容

格式如下：

---

## ${CURRENT_TIME}

### 本次任务
一句话描述本次工作的核心目标

### 完成内容
- xxx
- xxx
- xxx

### 修改文件
- xxx
- xxx

### 核心技术决策
- xxx

### 风险与问题
- xxx

### 下一步计划
- xxx

### Git状态摘要
- 当前分支：xxx
- 变更文件数：xxx

---

要求内容尽可能具体，基于实际代码变更进行总结。
EOF
)

########################################
# 调用 Claude
########################################
SUMMARY=$(claude -p "$PROMPT_CONTENT")

########################################
# 追加到日志
########################################
echo "$SUMMARY" >> "$WORKLOG_FILE"

echo ""
echo "========================================"
echo "✅ 工作总结已保存"
echo "📄 文件：$WORKLOG_FILE"
echo "🕒 时间：$NOW"
echo "========================================"